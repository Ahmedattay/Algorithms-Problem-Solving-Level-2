#include <iostream>
using namespace std;
int main()
{
    cout << "\t\t1\t2\t3\t4\t5\t6\t7\t8\t9\t10\n";
    cout << "----------------------------------------------------------------------------------------------\n";
    for (int j = 1; j <= 10; j++)
    {
        cout << j << "\t|";
        for (int i = 1; i <= 10; i++)
        {
            cout << "\t" << j * i;
        }
        cout << "\n";
    }
    cout << "----------------------------------------------------------------------------------------------\n";
    return 0;
}